CREATE OR REPLACE FUNCTION public.sync_creator_request_to_user()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
begin
  -- APPROVED → enable creator
  if new.status = 'approved'
     and old.status is distinct from 'approved' then

    update public.users
    set
      is_creator = true,
      creator_username = new.creator_username,
      platform_title = new.platform_title,
      creator_description = new.platform_description,
      creator_avatar_url = new.creator_avatar_url,
      updated_at = now()
    where id = new.user_id;

  -- REVOKED / REJECTED → disable creator
  elsif new.status in ('rejected', 'revoked')
        and old.status is distinct from new.status then

    update public.users
    set
      is_creator = false,
      creator_username = null,
      platform_title = null,
      creator_description = null,
      creator_avatar_url = null,
      updated_at = now()
    where id = new.user_id;

  end if;

  return new;
end;
$function$
;

