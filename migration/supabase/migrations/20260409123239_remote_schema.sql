CREATE OR REPLACE FUNCTION public.increment_view_counts(p_post_id uuid, p_video_part_id uuid, p_viewer_id uuid, p_creator_id uuid)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
begin

  IF p_video_part_id IS NOT NULL THEN

    insert into post_views(video_part_id, viewer_id, viewed_at)
    values (p_video_part_id, p_viewer_id, now())
    on conflict do nothing;

    update video_parts
    set views_count = (
      select count(*) from post_views where video_part_id = p_video_part_id
    )
    where id = p_video_part_id;

    RAISE LOG 'VIDEO PART VIEW COUNT UPDATED %', p_video_part_id;

  ELSE

    insert into post_views(post_id, viewer_id, viewed_at)
    values (p_post_id, p_viewer_id, now())
    on conflict do nothing;

    update posts
    set views_count = (
      select count(*) from post_views where post_id = p_post_id
    )
    where id = p_post_id;

    RAISE LOG 'POST VIEW COUNT UPDATED %', p_post_id;

  END IF;


  update users
  set creator_views = (
    select coalesce(sum(views_count),0)
    from posts
    where user_id = p_creator_id
  )
  where id = p_creator_id;

end;
