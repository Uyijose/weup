CREATE OR REPLACE FUNCTION public.add_anon_view(p_anon_id uuid, p_post_id uuid, p_video_part_id uuid)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
BEGIN
    -- Only insert if no existing row matches anon_id + post_id or anon_id + video_part_id
    INSERT INTO public.post_views (anon_id, post_id, video_part_id)
    SELECT p_anon_id, p_post_id, p_video_part_id
    WHERE NOT EXISTS (
        SELECT 1
        FROM public.post_views pv
        WHERE pv.anon_id = p_anon_id
          AND (
                (p_post_id IS NOT NULL AND pv.post_id = p_post_id)
             OR (p_video_part_id IS NOT NULL AND pv.video_part_id = p_video_part_id)
          )
    );
END;
$function$
