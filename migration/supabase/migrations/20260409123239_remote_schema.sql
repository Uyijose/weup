CREATE OR REPLACE FUNCTION public.update_post_likes_count()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
BEGIN

  IF TG_OP = 'INSERT' THEN

    IF NEW.video_part_id IS NOT NULL THEN
      UPDATE video_parts
      SET likes_count = likes_count + 1
      WHERE id = NEW.video_part_id;

      RAISE LOG 'VIDEO PART LIKE INSERT → COUNT +1 %', NEW.video_part_id;

    ELSE
      UPDATE posts
      SET likes_count = likes_count + 1
      WHERE id = NEW.post_id;

      RAISE LOG 'POST LIKE INSERT → COUNT +1 %', NEW.post_id;
    END IF;

  END IF;


  IF TG_OP = 'DELETE' THEN

    IF OLD.video_part_id IS NOT NULL THEN
      UPDATE video_parts
      SET likes_count = GREATEST(likes_count - 1,0)
      WHERE id = OLD.video_part_id;

      RAISE LOG 'VIDEO PART LIKE DELETE → COUNT -1 %', OLD.video_part_id;

    ELSE
      UPDATE posts
      SET likes_count = GREATEST(likes_count - 1,0)
      WHERE id = OLD.post_id;

      RAISE LOG 'POST LIKE DELETE → COUNT -1 %', OLD.post_id;
    END IF;

  END IF;

  RETURN NULL;

END;
$function$
;

