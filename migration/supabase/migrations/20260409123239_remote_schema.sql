CREATE OR REPLACE FUNCTION public.handle_post_view_delete()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
declare
  post_owner uuid;
begin
  select user_id into post_owner
  from posts
  where id = old.post_id;

  if old.viewer_id = post_owner then
    return old;
  end if;

  update posts
  set views_count = views_count - 1
  where id = old.post_id;

  update users
  set videos_watched = videos_watched - 1
  where id = old.viewer_id;

  update users
  set creator_views = creator_views - 1
  where id = post_owner;

  return old;
end;
$function$
;

