CREATE OR REPLACE FUNCTION public.handle_subscription_count()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
begin
  if tg_op = 'INSERT' then
    update users
    set subscribers_count = subscribers_count + 1
    where id = new.creator_id;

    update users
    set subscriptions_count = subscriptions_count + 1
    where id = new.subscriber_id;

  elsif tg_op = 'DELETE' then
    update users
    set subscribers_count = subscribers_count - 1
    where id = old.creator_id;

    update users
    set subscriptions_count = subscriptions_count - 1
    where id = old.subscriber_id;
  end if;

  return null;
end;
$function$
