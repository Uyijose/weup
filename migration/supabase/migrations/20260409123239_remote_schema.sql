CREATE OR REPLACE FUNCTION public.increment_post_views(p_post_id text, p_viewer_id text, p_creator_id text)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
begin
  insert into post_views(post_id, viewer_id)
  values (p_post_id::uuid, p_viewer_id::uuid)
  on conflict (post_id, viewer_id) do nothing;

  update posts
  set views_count = (
      select count(*) from post_views where post_id = p_post_id::uuid
  )
  where id = p_post_id::uuid;

  update users
  set creator_views = (
      select coalesce(sum(views_count),0) from posts where user_id = p_creator_id::uuid
  )
  where id = p_creator_id::uuid;
end;
$function$
;

CREATE OR REPLACE FUNCTION public.increment_post_views_anonymous(p_post_id uuid, p_anon_id uuid, p_video_part_id uuid DEFAULT NULL::uuid)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
declare
  already_viewed boolean;
begin
  select exists (
    select 1
    from post_views
    where post_id = p_post_id
      and anonymous_views ->> 'anon_id' = p_anon_id::text
  ) into already_viewed;

  if already_viewed then
    raise notice 'ANON VIEW SKIPPED %', p_anon_id;
    return;
  end if;

  insert into post_views (
    post_id,
    video_part_id,
    anonymous_views
  )
  values (
    p_post_id,
    p_video_part_id,
    jsonb_build_object('anon_id', p_anon_id)
  );

  update posts
  set views_count = views_count + 1
  where id = p_post_id;

  raise notice 'ANON VIEW SAVED %', p_anon_id;
end;
$function$
;

