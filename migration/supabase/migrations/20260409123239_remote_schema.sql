CREATE OR REPLACE FUNCTION public.increment_view_counts(p_post_id uuid, p_viewer_id uuid, p_creator_id uuid)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
begin
  insert into post_views(post_id, viewer_id, viewed_at)
    values (p_post_id, p_viewer_id, now())
